     <p>I've been involved with ATS Mentoring since September 2019. ATS Mentoring focuses on introducing first years to life at University by organising events, guided tours on the first week of University and sets up WhatsApp group chats to help them connect with their peers. These group chats are also used for first years to ask questions to their mentors who are usually 2nd/3rd/4th year students who study the same course as them. The ATS programme also helps HEAR (students underepresented in 3rd level education due to socio economic factors) and DARE (Students with disabilites) students by making them aware of the variety of resources available in NUI Galway.</p>
   
